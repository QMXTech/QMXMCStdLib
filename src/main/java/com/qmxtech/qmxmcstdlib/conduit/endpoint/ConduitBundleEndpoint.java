import com.qmxtech.qmxmcstdlib.position.DimensionalPosition;

import net.minecraft.util.EnumFacing;

import java.util.UUID;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// The 'ConduitBundleEndpoint' Class
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

public class ConduitBundleEndpoint extends Endpoint
{
    // Public Constructor

        public ConduitBundleEndpoint( final UUID identifier, final String label, final DimensionalPosition position, final EnumFacing side )
        {
            super( identifier, label, position, side );
        }

        public ConduitBundleEndpoint( final String label, final DimensionalPosition position, final EnumFacing side )
        {
            super( label, position, side );
        }

        public ConduitBundleEndpoint( final DimensionalPosition position, final EnumFacing side )
        {
            super( position, side );
        }

    // Public Methods

        @Override public String getChannelType()
        {
            return "bundle";
        }

        @Override public String getEndpointType()
        {
            return "bundle";
        }
}


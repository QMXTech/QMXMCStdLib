import net.minecraftforge.fluids.capability.IFluidHandler;
import net.minecraftforge.fluids.FluidStack;
import java.util.UUID;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// The 'FluidChannel' Class
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

public class FluidChannel extends FilterableChannel< FluidStack, IFluidHandler > implements IFluidChannel
{
    // Public Constants

        public static final String TYPE = "fluid";

    // Public Constructor

        public FluidChannel( final UUID identifier, final String label )
        {
            super( identifier, label, TYPE );
        }

        public FluidChannel( final String label )
        {
            super( label, TYPE );
        }

        public FluidChannel()
        {
            super( TYPE );
        }

} // endpoint has Insert, Extract{ Redstone }

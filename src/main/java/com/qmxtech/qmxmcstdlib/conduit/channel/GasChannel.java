import mekanism.api.gas.GasStack;
import mekanism.api.gas.IGasHandler;

import java.util.UUID;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// The 'GasChannel' Class
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

public class GasChannel extends FilterableChannel< GasStack, IGasHandler > implements IGasChannel
{
    // Public Constants

        public static final String TYPE = "gas";

    // Public Constructor

        public GasChannel( final UUID identifier, final String label )
        {
            super( identifier, label, TYPE );
        }

        public GasChannel( final String label )
        {
            super( label, TYPE );
        }

        public GasChannel()
        {
            super( TYPE );
        }

} // endpoint has Insert, Extract{ Redstone }
// filter by gas


import com.qmxtech.qmxmcstdlib.world.IHasWorld;

import net.minecraft.world.World;
import net.minecraftforge.common.DimensionManager;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// The 'IDimensionalPosition' Interface
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

public interface IDimensionalPosition extends IHasPosition, IHasWorld
{
    // Methods

        // WARNING: getWorld() and getDimension() call each other by default. One MUST be overridden to avoid an error (use IHasDimensionalPosition preferably).
        // Note: When IHasDimensionalPosition is implemented in a TileEntity, this call is ignored unless explicitly called (via Interface.super).
        @Override default World getWorld()
        {
            return (World) DimensionManager.getWorld( getDimension() );
        }

        default boolean isNeighbor( final IDimensionalPosition position )
        {
            return ( getDimension() == position.getDimension() ) && IHasPosition.super.isNeighbor( position );
        }
}

